@OptIn(
    ExperimentalMaterial3ComponentOverrideApi::class,
    ExperimentalMaterial3ExpressiveApi::class,
    ExperimentalMaterial3AdaptiveComponentOverrideApi::class
)
@Composable
actual fun AnilibriaTheme(
    darkTheme: Boolean,
    dynamicColor: Boolean,
    content: @Composable () -> Unit
) {
    val colorScheme = rememberColorScheme(darkTheme, dynamicColor)

    CompositionLocalProvider(
        LocalNavigationSuiteScaffoldWithPrimaryActionOverride provides AnilibriaNavigationSuiteScaffold
    ) {
        MaterialExpressiveTheme(
            colorScheme = colorScheme,
            motionScheme = MotionScheme.expressive(),
            typography = AnilibriaTypography(),
            content = content,
        )
    }
}

@Composable
actual fun rememberColorScheme(
    darkTheme: Boolean,
    dynamicColor: Boolean
): ColorScheme {
    return when {
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            val context = LocalContext.current
            val tonalPalette = dynamicTonalPalette(context)
            dynamicColorScheme(
                seedColor = tonalPalette.primary80,
                isDark = darkTheme,
                style = PaletteStyle.Expressive,
                specVersion = ColorSpec.SpecVersion.SPEC_2025,
            )
        }
        else -> selectSchemeForContrast(darkTheme)
    }
}

@Composable
internal fun selectSchemeForContrast(darkTheme: Boolean): ColorScheme {
    val context = LocalContext.current
    var colorScheme = if (darkTheme) darkScheme else lightScheme
    if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.UPSIDE_DOWN_CAKE) {
        val uiModeManager = context.getSystemService(Context.UI_MODE_SERVICE) as UiModeManager
        val contrastLevel = uiModeManager.contrast

        colorScheme = when (contrastLevel) {
            in 0.0f..0.33f -> if (darkTheme) darkScheme else lightScheme

            in 0.34f..0.66f -> if (darkTheme)
                mediumContrastDarkColorScheme else mediumContrastLightColorScheme

            in 0.67f..1.0f -> if (darkTheme)
                highContrastDarkColorScheme else highContrastLightColorScheme

            else -> if (darkTheme) darkScheme else lightScheme
        }
        return colorScheme
    } else return colorScheme
}

